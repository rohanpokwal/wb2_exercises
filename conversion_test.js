"use strict";

//the variables we are working with
let a = " 101.1 ";
let b = "55";
let c = "402 Stevens";
let d = "Number 5 ";

//using parseInt for Each variable and displaying the results
console.log("var a with parseInt: ", parseInt(a));
console.log("var a with parseInt: ", parseInt(b));
console.log("var a with parseInt: ", parseInt(c));
console.log("var a with parseInt: ", parseInt(d));

//using parseFloat for Each variable and displaying the results
console.log("var a with parseFloat: ", parseFloat(a));
console.log("var a with parseFloat: ", parseFloat(b));
console.log("var a with parseFloat: ", parseFloat(c));
console.log("var a with parseFloat: ", parseFloat(d));

//using Number for Each variable and displaying the results
console.log("var a with Number: ", Number(a));
console.log("var a with Number: ", Number(b));
console.log("var a with Number: ", Number(c));
console.log("var a with Number: ", Number(d));

//using unary+ for Each variable and displaying the results
console.log("var a with Unary: ", +a);
console.log("var a with Unary: ", +b);
console.log("var a with Unary: ", +c);
console.log("var a with Unary: ", +d);
